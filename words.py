ALL_WORDS: list[tuple[str, str]] = [
    (word, cat)
    for cat, words in WORDS.items()
    for word in words
]


def get_random_word(
    used_words: set | None = None,
    category: str | None = None,
) -> tuple:
    import random

    if used_words is None:
        used_words = set()

    if category and category in WORDS:
        pool = [(w, category) for w in WORDS[category] if w not in used_words]
    else:
        pool = [(w, cat) for w, cat in ALL_WORDS if w not in used_words]

    if not pool:
        return None, None

    return random.choice(pool)
